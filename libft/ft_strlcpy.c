#include "libft.h"

size_t	strlcpy(char *dst, const char *src, size_t siz)
{
	size_t i;
	size_t res;

	res = ft_strlen(dst);
	if (res > siz)
	{
		i = 0;
		while (src[i])
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	return (res);
}
