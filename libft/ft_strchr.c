#include "libft.h"

char	*ft_strchr(const char *str, int ch)
{
	int		i;
	char	c;
	char	*s;

	i = 0;
	c = (char)ch;
	s = (char *)str;
	while (s[i])
	{
		if (s[i] == c)
			return (s + i);
		i++;
	}
	return (NULL);
}
