#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int i;
	size_t len;
	char *res;

	i = 0;
	res = NULL;
	len = ft_strlen(s);
	res = (char *)ft_calloc(sizeof(char), len + 1);
	if (res != NULL)
	{
		while (s[i])
		{
			res[i] = f(i, s[i]);
			i++;
		}
		res[i] = '\0';
	}
	return (res);
}
